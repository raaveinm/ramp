@Composable
fun ChirroTheme(
    appTheme: AppTheme = AppTheme.DYNAMIC,
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
  val colorScheme = when(appTheme) {
      AppTheme.DYNAMIC -> {
          if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.S) {
              val context = LocalContext.current
              if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
          } else {
              if (darkTheme) darkScheme else lightScheme
          }
      }
      AppTheme.LIGHT -> lightScheme
      AppTheme.DARK -> darkScheme
      AppTheme.PINK -> if (darkTheme) PinkColors.pinkDarkScheme else PinkColors.pinkLightScheme
      AppTheme.HIGH_CONTRAST -> if (darkTheme) highContrastDarkColorScheme else highContrastLightColorScheme
      AppTheme.MONOCHROME -> if (darkTheme) MonochromeColors.monochromeDarkScheme else MonochromeColors.monochromeLightScheme
  }

  MaterialTheme(
    colorScheme = colorScheme,
    typography = AppTypography,
    content = content
  )
}